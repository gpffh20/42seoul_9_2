int	ft_is_prime(int nb)
{
	int	i;
	int	res;

	i = 1;
	res = 0;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (0);
	while (i <= nb / i)
	{
		if (nb % i == 0)
			res += 2;
		i++;
	}
	if (res > 2)
		return (0);
	else
		return (1);
}
